const DEFAULT_SECONDARY_FEE = 110;

function normalize(s: string): string {
  return s.toLowerCase().replace(/\s+/g, " ").trim();
}

export function findSecondaryFee(schoolName: string): number {
  const name = normalize(schoolName);
  for (const entry of SCHOOL_SECONDARY_FEES) {
    if (name.includes(normalize(entry.match))) return entry.fee;
  }
  return DEFAULT_SECONDARY_FEE;
}
